public class bean {
    private String velocity;
    private String initialVelocity;
    private String acceleration;
    private String time;

    public bean() {
    }

    public String getVelocity() {
        return velocity;
    }

    public void setVelocity(final String velocity) {
        this.velocity = velocity;
    }

    public String getInitialVelocity() {
        return initialVelocity;
    }

    public void setInitialVelocity(final String initialVelocity) {
        this.initialVelocity = initialVelocity;
    }

    public String getAcceleration() {
        return acceleration;
    }

    public void setAcceleration(final String acceleration) {
        this.acceleration = acceleration;
    }

    public String getTime() {
        return time;
    }

    public void setTime(final String time) {
        this.time = time;
    }
}